package main;
/**
 * A simple Calculator class. 
 * Helps to illustrate how unit test works.
 */
public class Calculator {
    /**
     * Adds two numbers.
     * @param a the first number
     * @param b the second number
     * @return the sum of a and b
     */
    public int add(int a, int b) {
        return a + b;
    }
}
